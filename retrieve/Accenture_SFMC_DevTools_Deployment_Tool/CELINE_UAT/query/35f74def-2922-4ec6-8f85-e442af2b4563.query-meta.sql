SELECT
    A.PersonContactId AS ContactKey,
    A.Id AS 'Account Id',
    A.cel_ext_account_id__c AS 'External Id',
    A.Salutation AS Title,
    A.LastName AS Lastname,
    A.Firstname AS Firstname,
    A.cel_lastname_local_2__c AS 'Lastname Local',
    A.cel_firstname_local_2__c AS 'Firstname Local',
    A.cel_addr_1_validity__c AS 'Address 1 Validity',
    A.cel_addr_1_line_1__c AS 'Address 1 Line 1',
    A.cel_addr_1_line_2__c AS 'Address 1 Line 2',
    A.cel_addr_1_line_3__c AS 'Address 1 Line 3',
    A.cel_addr_1_zipcode__c AS 'Address 1 PostCode',
    A.cel_addr_1_region__c AS 'Address 1 State Province',
    A.cel_addr_1_city__c AS 'Address 1 City',
    A.cel_addr_1_country__c AS 'Address 1 Country',
    A.PersonMobilePhone AS 'Mobile Phone',
    A.PersonDoNotCall AS 'Mobile Optin',
    A.cel_mobile_validity__c AS 'Mobile Validity',
    A.PersonEmail AS Email,
    A.cel_do_not_email__c AS 'Email Optin',
    A.cel_second_email_optin__c AS 'Email Optin 2',
    A.cel_birthdate_day__c AS 'Birthdate Day',
    A.cel_birthdate_month__c AS 'Birthdate Month',
    A.cel_birthdate_year__c AS 'Birthdate Year',
    A.cel_customer_store_code_creation__c AS 'Creation Store',
    A.cel_customer_main_store__c AS 'Main Store',
    A.cel_customer_main_store__c AS 'Region Main Store',
    A.Type AS 'Customer Type',
    A.cel_origine__c AS 'Origin',
    A.cel_main_SA__c AS 'Main SA Contact Id',
    A.cel_nationality__c AS 'Nationality',
    A.cel_local_customer__c AS 'Customer Local',
    A.cel_segmentation__c AS 'Current Segmentation',
    A.cel_segmentation_last_month__c AS 'Previous Segmentation',
    A.cel_distance_nearest_store__c AS 'Distance of nearest store',
    A.cel_FLEN_Active__c AS 'Flag Flen',
    A.cel_sent_by_sfcm__c AS 'IsMCcust',
    A.cel_FLEN__c AS 'Flen',
    A.cel_creation_date__c AT TIME ZONE 'Central America Standard Time' AT TIME ZONE 'GMT Standard Time' AS 'Local Creation Date',
    A.CreatedDate AT TIME ZONE 'Central America Standard Time' AT TIME ZONE 'GMT Standard Time' AS 'Salesforce Creation Date',
    A.LastModifiedDate AT TIME ZONE 'Central America Standard Time' AT TIME ZONE 'GMT Standard Time' AS 'Salesforce Last Update Date',
    B.FirstName AS 'Main SA Firstname',
    B.LastName AS 'Main SA Lastname',
    B.cel_ext_sale_associate_code__c AS 'Main SA Cegid Id',
    SUBSTRING (A.cel_customer_main_store__c, 1, 2) AS 'Country Main Store',
    A.cel_id_check__c AS 'IDCheck'
FROM
    ENT.ACCOUNT_SALESFORCE AS A
    LEFT OUTER JOIN ENT.CONTACT_SALESFORCE B ON A.cel_main_SA__c = B.Id
WHERE
    A.RecordTypeId = '0121r0000007NppAAE'
    AND PersonEmail <> ''
    AND cel_ext_account_id__c <> ''